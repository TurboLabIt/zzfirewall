<?php
require 'generate.lib.php';

echo "‚öôÔ∏è Adding banner..." . PHP_EOL;
$txtWhitelist  = '## ‚ò†Ô∏è WARNING! This file was AUTOGENERATED by zzfirewall - Do not edit directly!' . PHP_EOL;
$txtWhitelist .= '## See: https://github.com/TurboLabIt/zzfirewall/';


echo "‚öôÔ∏è Allow from GoogleBot (search)..." . PHP_EOL;
// https://developers.google.com/search/docs/crawling-indexing/verifying-googlebot
const GOOGLEBOT_URL = 'https://developers.google.com/static/search/apis/ipranges/googlebot.json';
$txtWhitelist .= PHP_EOL . PHP_EOL . '## üîé Allow from GoogleBot - ' . GOOGLEBOT_URL . PHP_EOL;

$txtGoogle = implode(PHP_EOL, getGoogleIpList(GOOGLEBOT_URL) ) . PHP_EOL;
$txtWhitelist .= $txtGoogle;


/**
 * Allow connections from Uptimerobot...
 * ===================================
 */
// https://uptimerobot.com/help/locations/
const UPTIMEROBOT_WHITELIST_URL = 'https://uptimerobot.com/inc/files/ips/IPv4.txt';
echo "‚öôÔ∏è Adding from UptimeRobot ..." . PHP_EOL;
$txtWhitelist .= PHP_EOL . PHP_EOL . '## üîé Allow from UptimerobotBot - ' . UPTIMEROBOT_WHITELIST_URL . PHP_EOL;
$txtUptimerobot = file_get_contents(UPTIMEROBOT_WHITELIST_URL);

if($txtUptimerobot === false) {
    die("‚ö†Ô∏è Download from " . UPTIMEROBOT_WHITELIST_URL . " FAILED! Aborting!");
}

$txtWhitelist .= $txtUptimerobot;


/**
 * Allow connections from Bunny CDN...
 * ===================================
 */
// https://bunnycdn.com/api/system/edgeserverlist
const BUNNYCDN_WHITELIST_URL = 'https://bunnycdn.com/api/system/edgeserverlist';
echo "‚öôÔ∏è Adding from Bunny CDN ..." . PHP_EOL;
$txtWhitelist .= PHP_EOL . PHP_EOL . '## üîé Allow from Bunny CDN - ' . BUNNYCDN_WHITELIST_URL . PHP_EOL;
$oXmlBunnyCdn = simplexml_load_file(BUNNYCDN_WHITELIST_URL);

if($oXmlBunnyCdn === false) {
    die("‚ö†Ô∏è Parsing XML from " . BUNNYCDN_WHITELIST_URL . " FAILED! Aborting!");
}

// The XML uses a default namespace, so we must register it to query elements.
// The namespace URI matches the 'xmlns' attribute in the root <ArrayOfstring> element.
$oXmlBunnyCdn->registerXPathNamespace('ns', 'http://schemas.microsoft.com/2003/10/Serialization/Arrays');
$oBunnyCdnIps = $oXmlBunnyCdn->xpath('//ns:string');
foreach($oBunnyCdnIps as $ip) {
    // Cast the SimpleXMLElement object to a string and append
    $txtWhitelist .= (string)$ip . PHP_EOL;
}


/**
 * Writing the file...
 * ===================
 */
const WHITELIST_OUT_PATH = '/usr/local/turbolab.it/zzfirewall/lists/autogen/';

$filePath = WHITELIST_OUT_PATH . 'whitelist.txt';
echo "‚öôÔ∏è Writing the file to " . $filePath . "..." . PHP_EOL;
file_put_contents($filePath, $txtWhitelist);

$filePath = WHITELIST_OUT_PATH . 'google-search.txt';
echo "‚öôÔ∏è Writing the file to " . $filePath . "..." . PHP_EOL;
file_put_contents($filePath, $txtGoogle);


echo "‚öôÔ∏è Writing all Google IP list (all services, including public cloud) - not whitelisted..." . PHP_EOL;
