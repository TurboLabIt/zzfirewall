  die("‚ö†Ô∏è Download from " . GOOGLE_WHITELIST_URL . " FAILED! Aborting!");
}

$arrGoogle = json_decode($txtGoogle);

if( !is_object($arrGoogle) ) {
  die("‚ö†Ô∏è json_decode from " . GOOGLE_WHITELIST_URL . " FAILED! Aborting!");
}

